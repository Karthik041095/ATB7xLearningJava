package ex_13072024;

public class Lab023 {
    public static void main(String[] args) {
        // Formatting
        int age = 98;
        String bal = "Karthik";
        System.out.println("You age is -> " + age);
        // %d ->  any integer - byte, short, int or long
        // %s -> String
        // %c -> char
        // %f -> float
        System.out.printf("You age is -> %d",age);
        System.out.println();
        System.out.printf("My name is -> %s",bal);

    }
}
